    private void tampildata(){
     try {
           String sql = "UPDATE admin SET nama = '" + txNama.getText() + "', password = '"+ txPass.getText() + "', "
                        + "alamat = '"+txAlamat.getText()+ "', notelp = '"+txNotelp.getText()+ "', email = '"+txEmail.getText()+"', posisi ='"+ 
                        cboxPosisi.getSelectedItem().toString() + "' WHERE user_id ='" + txId.getText() + "'";
         try (PreparedStatement p = conn.prepareStatement(sql)) {
             p.executeUpdate();
         }
            } catch (SQLException e) {
                System.out.println("Terjadi Error");
            } finally {
                 try {
                    String sql = "SELECT * FROM admin WHERE username = '" + txUsername.getText() + "' AND user_id = '" + txId.getText() + "'";
                    Statement st = conn.createStatement();
                    ResultSet rs = st.executeQuery(sql);
                    while (rs.next()) {
                    txId.setText(rs.getString("user_id"));
                    txUsername.setText(rs.getString("username"));
                    txPass.setText(rs.getString("password"));
                    txNama.setText(rs.getString("nama"));
                    txAlamat.setText(rs.getString("alamat"));
                    txNotelp.setText(rs.getString("notelp"));
                    txEmail.setText(rs.getString("email"));
                    cboxPosisi.setSelectedItem(rs.getString("posisi"));
            }    
            
        } catch (SQLException ex) {
            Logger.getLogger(InfoUser.class.getName()).log(Level.SEVERE, null, ex);
        }
                off();
                JOptionPane.showMessageDialog(null, "Data Berhasil Diubah", "Kedai Avicenna", JOptionPane.INFORMATION_MESSAGE);
            }
}
    
